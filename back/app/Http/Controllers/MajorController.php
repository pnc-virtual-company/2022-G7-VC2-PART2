     * @param  \App\Models\Major  $major
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $major = Major::findOrFail($id);
        $major->name = $request->name;
        
        $major->save();
        return response()->json(['message'=>'Update']);
    }

